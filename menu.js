const selectedRestaurant = localStorage.getItem("selectedRestaurant");
const menuItems = menus[selectedRestaurant] || [];
const restaurantName = document.getElementById("restaurant-name");
restaurantName.textContent = `Menu`;
const menuItemsContainer = document.getElementById("menu-items");

menuItems.forEach((item, index) => {
    const div = document.createElement("div");
    div.classList.add("menu-item");
    div.innerHTML = `
        <img src="${item.image}" alt="${item.name}">
        <h4>${item.name}</h4>
        <p>₹${item.price}</p>
        <button onclick="addToCart(${index})">Add to Cart</button>
    `;
    menuItemsContainer.appendChild(div);
});

function addToCart(index) {
    const cart = JSON.parse(localStorage.getItem("cart")) || [];
    cart.push(menuItems[index]);
    localStorage.setItem("cart", JSON.stringify(cart));
    alert(`${menuItems[index].name} added to cart!`);
}
